package tres.ejemplos;

public class ejemplo11 {

    public static void main(String[] args) {

          for (int i = 1; i <= 10; i++) {
            System.out.print("Tabla de " + i + ": ");
            for (int j = 1; j <= i; j++) {
                System.out.print(i * j + " ");
            }
            System.out.println();
        }
    }
        
    }
    

