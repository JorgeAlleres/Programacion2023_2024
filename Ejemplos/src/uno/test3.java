package uno;

public class test3 {

    public static void main(String[] args) {
        
        int m=2, n=5;
        boolean res;
        
        res =m > n;//res=false
        System.out.println(res);

        res =m < n;//res=true
        System.out.println(res);

        res =m >= n;//res=false
        System.out.println(res);

        res =m <= n;//res=true
        System.out.println(res);

        res =m == n;//res=false
        System.out.println(res);

        res =m != n;//res=true
        System.out.println(res);

    }
    
}
