public class resuelto6 {

    public static void main(String[] args) {

        //con el i=i+2 ir√° contando de dos en dos
        for(int i=200; i<=300; i=i+2) {
            System.out.println("Numero: "+i);
        }

        System.out.println("");

        for(int j=300; j>=200; j--) {
            System.out.println("Numero: "+j);
        }

     
    }
    
}
