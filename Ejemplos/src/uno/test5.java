public class test5 {

    public static void main(String[] args) {
        
        for(int i=0; i<=10; i++) {

            System.out.println("i vale: "+i);

        }

        System.out.println("se acabo el bucle for ascendente");

        for(int j=10; j>=0; j--) {
            System.out.println("j vale: "+j);
        }

        System.out.println("se acabo el bucle for descendente");
    
    }
    
}
